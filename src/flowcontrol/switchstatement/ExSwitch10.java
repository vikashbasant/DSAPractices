package flowcontrol.switchstatement;

public class ExSwitch10 {
    public static void main(String[] args) {
        int x = 10;
        switch (x) {

            case 1:
            case 2:
            case 3:
                System.out.println("Q-1");
                break;
            case 4:
            case 5:
            case 6:
                System.out.println("Q-2");
                break;
            case 7:
            case 8:
            case 9:
                System.out.println("Q-3");
                break;
            case 10:
            case 11:
            case 12:
                System.out.println("Q-4");
                break;
        }
    }
}
